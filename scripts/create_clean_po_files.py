def create_po_file(lang_code, translations):
    """Cria um arquivo .po limpo para o idioma especificado"""
    po_content = f'''# {lang_code.upper()} translation of ForgeLock
# Copyright (C) 2025 ForgeLock
# This file is distributed under the same license as the ForgeLock package.
msgid ""
msgstr ""
"Project-Id-Version: ForgeLock 1.0\\n"
"Report-Msgid-Bugs-To: \\n"
"POT-Creation-Date: 2025-01-27 10:00+0000\\n"
"PO-Revision-Date: 2025-01-27 10:00+0000\\n"
"Last-Translator: \\n"
"Language: {lang_code}\\n"
"MIME-Version: 1.0\\n"
"Content-Type: text/plain; charset=UTF-8\\n"
"Content-Transfer-Encoding: 8bit\\n"
"Plural-Forms: nplurals=2; plural=(n != 1);\\n"

'''
    
    # Adicionar todas as traduções
    for msgid, msgstr in translations.items():
        po_content += f'msgid "{msgid}"\n'
        po_content += f'msgstr "{msgstr}"\n\n'
    
    return po_content

def main():
    print("🔧 Criando Arquivos PO Limpos - ForgeLock")
    print("=" * 50)
    
    for lang_code, translations in TRANSLATIONS.items():
        print(f"\n📝 Criando arquivo para {lang_code}...")
        
        # Criar diretório se não existir
        po_dir = Path(f"locale/{lang_code}/LC_MESSAGES")
        po_dir.mkdir(parents=True, exist_ok=True)
        
        # Criar arquivo .po
        po_file = po_dir / "django.po"
        po_content = create_po_file(lang_code, translations)
        
        with open(po_file, 'w', encoding='utf-8') as f:
            f.write(po_content)
        
        print(f"   ✅ Arquivo criado: {po_file}")
        print(f"   📊 {len(translations)} traduções incluídas")
    
    print(f"\n🎉 Arquivos PO criados com sucesso!")
    print(f"🔧 Próximos passos:")
    print(f"   1. Execute: python manage.py compilemessages")
    print(f"   2. Teste a aplicação no navegador")
    print(f"   3. Verifique se as traduções estão funcionando")

if __name__ == "__main__":
    main()
